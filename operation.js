//operatores aritmeticos basicos 

let counter = 100;

// 1. suma

counter = counter + 20;
console.log(counter);
counter += 20;

console.log(counter);

// 2. resta

counter = counter - 20;
console.log(counter);
counter -= 20;

console.log(counter);

// 3. multiplicacion

counter = counter * 20;
console.log(counter);
counter *= 20;

console.log(counter);

// 4. division

counter = counter / 20;
console.log(counter);
counter /= 20;

console.log(counter);
